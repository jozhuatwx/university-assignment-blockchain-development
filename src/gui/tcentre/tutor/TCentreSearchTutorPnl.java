    // Populate table data
    private void populateTblTutor() {
        // Get table model
        DefaultTableModel model = (DefaultTableModel) tblTutor.getModel();
        // Remove existing combobox items
        model.setRowCount(0);

        for (int i = 0; i < Main.tutorDatabase.getSize(); i++) {
            Tutor tutor = Main.tutorDatabase.getData(i);
            if (tutor.getBranchId() == branch.getId()) {
                model.addRow(new Object[] {String.format("%.0f", tutor.getId()), tutor.getName(), tutor.getPhoneNumber(), tutor.getEmail()});
            }
        }
    }

    // Filter data
    private void filter(String query){
        // Get table model and create sorter
        DefaultTableModel model = (DefaultTableModel) tblTutor.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tblTutor.setRowSorter(sorter);

        // Filter the data
        if (query.trim().length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
        }
    }

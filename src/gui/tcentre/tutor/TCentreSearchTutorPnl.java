    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // Get model of the table, then get selected row
        DefaultTableModel model = (DefaultTableModel) tblTutor.getModel();
        int selectedRowIndex = tblTutor.convertRowIndexToModel(tblTutor.getSelectedRow());

        // Get the tutorID
        double id = Double.parseDouble(model.getValueAt(selectedRowIndex, 0).toString());

        frame.navigateMenuPanel(new TCentreTutorRightMenuSecondVer(frame, branch));
        frame.navigateContentPanel(new TCentreEditTutorPnl(frame, branch, Main.tutorDatabase.findData(id)));
    }//GEN-LAST:event_btnEditActionPerformed


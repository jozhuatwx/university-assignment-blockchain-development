    // Populate table data
    private void populateTblBranch() {
        // Get table model
        DefaultTableModel model = (DefaultTableModel) tblBranch.getModel();
        // Remove existing table data
        model.setRowCount(0);

        for (int i = 0; i < Main.branchDatabase.getSize(); i++) {
            Branch branch = Main.branchDatabase.getData(i);
            model.addRow(new Object[] {String.format("%.0f", branch.getId()), branch.getName(), branch.getPhoneNumber(), branch.getAddress()});
        }
    }

    // Filter data
    private void filter(String query) {
        // Get table model and create sorter
        DefaultTableModel model = (DefaultTableModel) tblBranch.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tblBranch.setRowSorter(sorter);

        // Filter the data
        if (query.trim().length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
        }
    }


    private void btnTerminateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTerminateActionPerformed
        // Get model of the table, then get selected row
        DefaultTableModel model = (DefaultTableModel) tblBranch.getModel();
        int selectedRowIndex = tblBranch.convertRowIndexToModel(tblBranch.getSelectedRow());

        // Get the branchId
        double id = Double.parseDouble(model.getValueAt(selectedRowIndex, 0).toString());

        // Check if there is existing tutors
        boolean existingTutors = false;
        for (int i = 0; i < Main.tutorDatabase.getSize(); i++) {
            if (Main.tutorDatabase.getData(i).getBranchId() == id) {
                existingTutors = true;
                break;
            }
        }

        if (!existingTutors) {
            int confirmationResult = JOptionPane.showConfirmDialog(null,
                "Do you want to terminate this account?",
                "Terminate Tuition Centre",
                JOptionPane.YES_NO_OPTION);

            // Result of the confirmation message box
            if (confirmationResult == 0) {
                // Remove data from database
                Main.branchDatabase.removeData(id);

                // Display success message
                JOptionPane.showMessageDialog(new JFrame(), "Tutor Terminated Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

                // Refresh the page
                frame.navigateContentPanel(new HQSearchTCentrePnl(frame));
            }
        } else {
            // Display error message
            JOptionPane.showMessageDialog(new JFrame(), "There Are Existing Tutors", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTerminateActionPerformed

